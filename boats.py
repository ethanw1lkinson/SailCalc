def get_boat_definitions() -> list[dict]:
    """Return boat definitions in a form that is simple for the UI to render."""
    return [{"name": name, "handicap": handicap} for name, handicap in sorted(BOATS.items())]


def get_boat_handicap(boat_class: str) -> float:
    """Return the handicap for a boat class, or a default if it is unknown."""
    return BOATS.get(boat_class, 100.0)


def get_boat_classes() -> list[str]:
    """Return the known boat classes sorted alphabetically."""
    return sorted(BOATS.keys())
